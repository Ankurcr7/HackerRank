if __name__ == '__main__':
    l=["False","False","False","False","False"]
    s = input()
    for i in range(len(s)):
        if s[i].isalnum():
            l[0]="True"
        
        if s[i].isalpha():
            l[1]="True"
        
        if s[i].isdigit():
            l[2]="True"
        
        if s[i].islower():
            l[3]="True"
        
        if s[i].isupper():
            l[4]="True"
        
    for j in l:
        print(j)